package model;

/**
 *
 * @author ASUS
 */
public class Category {
     int cid;
    String categoryName;

    public Category() {
    }

    public Category(int cid, String categoryName) {
        this.cid = cid;
        this.categoryName = categoryName;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }
    
}
